import datetime
import os
from typing import Optional

from eval.run_eval import evaluate_all


def _fmt(value):
    if isinstance(value, float):
        return f"{value:.4f}"
    return str(value)


def _section_table(title: str, data: dict) -> str:
    lines = [f"## {title}", "", "| Metric | Value |", "|---|---|"]
    for key, value in data.items():
        lines.append(f"| `{key}` | `{_fmt(value)}` |")
    lines.append("")
    return "\n".join(lines)


def _build_conclusion(results: dict) -> str:
    privacy = results.get("privacy_session", {})
    redaction = privacy.get("redaction_success_rate")
    rotation = privacy.get("rotation_success_rate")
    tool_acc = results.get("llm_output", {}).get("tool_allowed_accuracy")
    tpr = results.get("input_filter", {}).get("block_recall_tpr")

    lines = ["## Conclusion", ""]
    if redaction == 1.0 and rotation == 1.0 and tool_acc == 1.0 and tpr == 1.0:
        lines.append(
            "- Current baseline shows strong secure-by-default behavior across injection filtering, tool governance, and privacy-session controls."
        )
    else:
        lines.append(
            "- Baseline is partially effective; some metrics still need hardening before interview/demo-grade release."
        )

    lines.extend(
        [
            "- Privacy session controls are evaluated separately to prove sensitive-field masking and per-tool key rotation are functioning.",
            "- Next improvement path: expand adversarial cases (multi-turn jailbreak, encoded prompt injection, and output exfiltration variants).",
            "",
        ]
    )
    return "\n".join(lines)


def _build_interview_talking_points() -> str:
    return "\n".join(
        [
            "## Interview Talking Points",
            "",
            "- `Threat Model`: Prompt injection, tool abuse, and secret exfiltration in LLM-agent pipelines.",
            "- `Defense Layers`: Input filter -> schema validation -> tool guard -> output filtering -> privacy session ratchet.",
            "- `Crypto Angle`: PCKA-style session state with post-tool rotation reduces single-key compromise impact.",
            "- `Measurability`: Metrics are generated by reproducible eval cases instead of anecdotal claims.",
            "",
        ]
    )


def generate_report(output_path: Optional[str] = None) -> str:
    results = evaluate_all()
    now = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    report_path = output_path or os.path.join(os.path.dirname(__file__), "eval_report.md")

    parts = [
        "# Sec_Agent Privacy-Security Evaluation Report",
        "",
        f"- Generated at: `{now}`",
        "",
    ]

    if "input_filter" in results:
        parts.append(_section_table("Input Filter Metrics", results["input_filter"]))
    if "llm_output" in results:
        parts.append(_section_table("LLM Output Governance Metrics", results["llm_output"]))
    if "privacy_session" in results:
        parts.append(_section_table("Privacy Session Metrics", results["privacy_session"]))

    parts.append(_build_conclusion(results))
    parts.append(_build_interview_talking_points())

    text = "\n".join(parts).strip() + "\n"
    with open(report_path, "w", encoding="utf-8") as f:
        f.write(text)
    return report_path


def main():
    path = generate_report()
    print(f"Report generated: {path}")


if __name__ == "__main__":
    main()
